package ArrayListExmpl;

import java.util.ArrayList;
public class ArryListExmp  // non generic collection
{

	public static void main(String[] args) 
	{
		ArrayList al = new ArrayList(); // declare ArrayList with initial capacity 10
		ArrayList s = new ArrayList();
		s.add(0.04f);
		s.add(100);
		al.add(55); // add elemet at end of list
		al.add("ram");
		al.add('A');
		al.addAll(s); // add collection s
		
		System.out.println("Array List "+ al);   // display Arralist
		System.out.println(al.size());
	}
}
